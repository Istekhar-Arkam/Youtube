// URL : Uniform resource Locator
// URI : Uniform resource Identifier
// URN : Uniform resource Name

// HTTP : Hyper text transfer protocol

// HTTP Method :

// GET     : To get resource.
// HEAD    : to get headers(no message body)
// OPTIONS : which operation are available
// TRACE   : loopback test(help in debugging)
// DELETE  : remove a resource
// PUT     : replace a resource similar like editing (replace the entire object)
// POST    : to add
// PATCH   : replace the particular part

// HTTP STATUS CODE

// 100 : Informational
// 200 : Success
// 300 : Redirection
// 400 : Client Error
// 500 : Server Error

// 100 Continue
// 102 Processing
// 200 Ok
// 201 Created
// 202 Accepted
// 307 Temporary redirect
// 308 Permanent redirect
// 400 Bad request
// 401 Unauthorized
// 402 Payment required
// 404 Not found
// 500 Internal server error
// 504 Gateway time out

// HTTPs : HTTPS stands for Hypertext Transfer Protocol Secure, which is the secure version of the HTTP protocol used for encrypted communication over a computer network
